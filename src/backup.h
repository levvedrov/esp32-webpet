void HandleBlinking()
{
  unsigned long now = millis();

  switch (blinkState)
  {
  case 0:

    if (now - blinkTimer >= BLINK_PERIOD)
    {
      blinkState = 1;
      blinkTimer = now;
      isBlinking = true;
    }
    break;

  case 1:

    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 2;
      blinkTimer = now;
      isBlinking = false;
    }
    break;

  case 2:

    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 3;
      blinkTimer = now;
      isBlinking = true;
    }
    break;

  case 3:
    // Вторая закрытая фаза
    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 4;
      blinkTimer = now;
      isBlinking = false;
    }
    break;

  case 4:

    if (now - blinkTimer >= BLINK_PERIOD)
    {
      blinkState = 1;
      blinkTimer = now;
      isBlinking = true;
    }
    break;
  }
}

bool isPressed()
{
  int touch = digitalRead(TOUCH_PIN);

  if (touch == 1)
    return true;
  else if (touch == 0)
    return false;
}

//

void setup()
{
  Serial.begin(115200);
  delay(1000);
  Serial.println("BOOTING...");
  Wire.begin(SDA_PIN, SCL_PIN);
  pinMode(TOUCH_PIN, INPUT);

  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
  {
    for (;;)
      ;
  }

  blinkTimer = millis();
  blinkState = 0;
  isBlinking = false;

  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(SSD1306_WHITE);
  display.setCursor(0, 0);
  display.println("LOADING");
  display.display();

  APsetup();
}

void loop()
{
  display.clearDisplay();

  HandleBlinking();
  HandleFactorsDecrease(&factors);

  _displayFactors(factors); // TMP

  if (isPressed())
  {
    Animations animation(display);
    animation.petted();
    increaseFactor(&factors.petting, 0.05);
  }

  else if (isBlinking)
    display.drawBitmap(STANDARD_SCREEN_OFFSET_X, STANDARD_SCREEN_OFFSET_Y, blinkedEyes, 96, 48, SSD1306_WHITE);
  else if (!isBlinking)
    display.drawBitmap(STANDARD_SCREEN_OFFSET_X, STANDARD_SCREEN_OFFSET_Y, openEyes, 96, 48, SSD1306_WHITE);

  display.display();
}
