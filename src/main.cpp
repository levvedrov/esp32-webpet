void setup()
{

  Serial.begin(115200);

  clearEEPROM(); // tmp

  delay(1000);
  Serial.println("BOOTING...");
  EEPROM.begin(EEPROM_SIZE);
  Wire.begin(SDA_PIN, SCL_PIN);
  pinMode(TOUCH_PIN, INPUT);

  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
  {
    for (;;)
      ;
  }

  blinkTimer = millis();
  blinkState = 0;
  isBlinking = false;

  if (hasSavedFactors())
  {
    loadFactors(factors);
    Serial.println("Loaded from memory!");
  }
  else
  {
    Serial.println("No saved data — using defaults!");
    factors = {80, 80, 80, 80};
    saveFactors(factors);
  }

  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(SSD1306_WHITE);
  display.setCursor(STANDARD_SCREEN_OFFSET_X, STANDARD_SCREEN_OFFSET_Y + 20);
  display.println("LOADING");
  display.display();

  // --- Загружаем конфиг тамагочи (имя + флаг вылупления) ---
  if (hasSavedConfig())
  {
    loadConfig(petConfig);
    Serial.printf("Config: name='%s', hatched=%d\n", petConfig.name, petConfig.hatched);
  }
  else
  {
    Serial.println("No pet config — creating new egg.");
    memset(&petConfig, 0, sizeof(petConfig));
    strncpy(petConfig.name, "Raf", sizeof(petConfig.name) - 1);
    petConfig.hatched = false;
    saveConfig(petConfig);
  }

  panel.setName(petConfig.name);
  randomSeed(esp_random());
  APsetup();
}

