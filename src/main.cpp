unsigned long factorTimer = 0;
uint8_t blinkState = 0;
bool isBlinking = false;

WebServer server(80);
Panel panel(&factors);

//
void _displayFactors(Factors f)
{
  Serial.println();
  Serial.println("STATUS: ");
  Serial.printf("OVERALL: %u%%\n", claculateOverall(f));

  Serial.printf("Hunger: %u%%\n", f.hunger);
  Serial.printf("Petting: %u%%\n", f.petting);
  Serial.printf("Attention: %u%%\n", f.attention);
  Serial.printf("Clean: %u%%\n", f.clean);
}

void HandleFactorsDecrease(Factors *f)
{
  unsigned long now = millis();
  if (now - factorTimer >= FACTOR_DECREASE_PERIOD)
  {
    f->attention *= ATTENTION_DECREASE_RATE;
    f->clean *= CLEAN_DECREASE_RATE;
    f->hunger *= HUNGER_DECREASE_RATE;
    f->petting *= PETTING_DECREASE_RATE;
    factorTimer = now;
  }
}

void increaseFactor(unsigned int *factor, float rate)
{
  if (*factor >= 100)
    return;

  float newValue = *factor + (rate);
  factors.attention += 10;

  if (factors.attention > 100.0f)
    factors.attention = 100.0f;

  if (newValue > 100.0f)
    newValue = 100.0f;

  *factor = (unsigned int)newValue;
}

void APsetup()
{

  WiFi.disconnect(true, true);
  delay(500);

  WiFi.mode(WIFI_AP_STA);

  bool ok = WiFi.softAP("Raf", nullptr, 1, 0);
  WiFi.setTxPower(WIFI_POWER_19_5dBm);

  Serial.print("[AP] ");
  Serial.println(ok ? "Started" : "FAIL");

  IPAddress ip = WiFi.softAPIP();
  Serial.print("[AP] IP: ");
  Serial.println(ip);
  server.on("/", []()
            { server.send(200, "text/html; charset=UTF-8", panel.render()); });
  server.on("/feed", []()
            { if (factors.hunger<100) animation.eating(); else animation.enoughEating(); increaseFactor(&factors.hunger, 25); server.send(200, "text/plain; charset=UTF-8", "OK"); });
  server.on("/clean", []()
            { increaseFactor(&factors.clean, 25); server.send(200, "text/plain; charset=UTF-8", "OK"); });

  server.begin();
  Serial.println("HTTP server started.");
}

void HandleBlinking()
{
  unsigned long now = millis();

  switch (blinkState)
  {
  case 0:

    if (now - blinkTimer >= BLINK_PERIOD)
    {
      blinkState = 1;
      blinkTimer = now;
      isBlinking = true;
    }
    break;

  case 1:

    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 2;
      blinkTimer = now;
      isBlinking = false;
    }
    break;

  case 2:

    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 3;
      blinkTimer = now;
      isBlinking = true;
    }
    break;

  case 3:
    // Вторая закрытая фаза
    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 4;
      blinkTimer = now;
      isBlinking = false;
    }
    break;

  case 4:

    if (now - blinkTimer >= BLINK_PERIOD)
    {
      blinkState = 1;
      blinkTimer = now;
      isBlinking = true;
    }
    break;
  }
}

bool isPressed()
{
  int touch = digitalRead(TOUCH_PIN);

  if (touch == 1)
    return true;
  else if (touch == 0)
    return false;
}

//

void setup()
{
  Serial.begin(115200);
  delay(1000);
  Serial.println("BOOTING...");
  Wire.begin(SDA_PIN, SCL_PIN);
  pinMode(TOUCH_PIN, INPUT);

  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
  {
    for (;;)
      ;
  }

  blinkTimer = millis();
  blinkState = 0;
  isBlinking = false;

  display.clearDisplay();
  display.setTextSize(2);
  display.setTextColor(SSD1306_WHITE);
  display.setCursor(0, 0);
  display.println("LOADING");
  display.display();

  APsetup();
}

void loop()
{
  display.clearDisplay();

  HandleBlinking();
  HandleFactorsDecrease(&factors);
  server.handleClient();

  _displayFactors(factors); // TMP

  if (isPressed())
  {

    animation.petted();
    increaseFactor(&factors.petting, 5);
  }

  else if (isBlinking)
    display.drawBitmap(STANDARD_SCREEN_OFFSET_X, STANDARD_SCREEN_OFFSET_Y, blinkedEyes, 96, 48, SSD1306_WHITE);
  else if (!isBlinking)
    display.drawBitmap(STANDARD_SCREEN_OFFSET_X, STANDARD_SCREEN_OFFSET_Y, openEyes, 96, 48, SSD1306_WHITE);

  display.display();
}
