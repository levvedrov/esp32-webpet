const uint32_t FACTORS_SIGNATURE = 0x52414646;

const uint32_t CONFIG_SIGNATURE = 0x5241464E; // 'RAFN'

// Конфиг: имя + флаг, что тамагочи уже вылупился
struct PetConfig
{
  char name[16];       // до 15 символов UTF-8 + '\0'
  bool hatched;        // 0 - яйцо, 1 - вылупился
  uint8_t reserved[3]; // выравнивание / запас
};

void saveConfig(const PetConfig &cfg)
{
  int addr = 64; // отдельный блок EEPROM
  uint32_t sig = CONFIG_SIGNATURE;
  EEPROM.put(addr, sig);
  addr += sizeof(sig);
  EEPROM.put(addr, cfg);
  EEPROM.commit();
}

bool hasSavedConfig()
{
  int addr = 64;
  uint32_t sig;
  EEPROM.get(addr, sig);
  return (sig == CONFIG_SIGNATURE);
}

void loadConfig(PetConfig &cfg)
{
  int addr = 64 + sizeof(CONFIG_SIGNATURE);
  EEPROM.get(addr, cfg);
}

typedef struct Factors
{
  unsigned int hunger, petting, attention, clean;
} Factors;

void clearEEPROM()
{
  EEPROM.begin(EEPROM_SIZE);
  for (int i = 0; i < EEPROM_SIZE; i++)
  {
    EEPROM.write(i, 0xFF);
  }
  EEPROM.commit();
}

void saveFactors(Factors &f)
{
  int addr = 0;

  EEPROM.put(addr, FACTORS_SIGNATURE);
  addr += sizeof(FACTORS_SIGNATURE);

  EEPROM.put(addr, f);
  EEPROM.commit();
}

bool hasSavedFactors()
{
  uint32_t sig;
  EEPROM.get(0, sig);
  return (sig == FACTORS_SIGNATURE);
}

void loadFactors(Factors &f)
{
  int addr = 0;
  addr += sizeof(FACTORS_SIGNATURE);
  EEPROM.get(addr, f);
}

