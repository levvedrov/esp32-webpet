void _displayFactors(Factors f)
{
  Serial.println();
  Serial.println("STATUS: ");
  Serial.printf("OVERALL: %u%%\n", claculateOverall(f));

  Serial.printf("Hunger: %u%%\n", f.hunger);
  Serial.printf("Petting: %u%%\n", f.petting);
  Serial.printf("Attention: %u%%\n", f.attention);
  Serial.printf("Clean: %u%%\n", f.clean);
}

void HandleFactorsDecrease(Factors *f)
{
  unsigned long now = millis();
  if (now - factorTimer >= FACTOR_DECREASE_PERIOD)
  {
    f->attention *= ATTENTION_DECREASE_RATE;
    f->clean *= CLEAN_DECREASE_RATE;
    f->hunger *= HUNGER_DECREASE_RATE;
    f->petting *= PETTING_DECREASE_RATE;
    factorTimer = now;
    saveFactors(*f);
  }
}

void increaseFactor(unsigned int *factor, float rate)
{
  if (*factor >= 100)
    return;

  float newValue = *factor + (rate);
  factors.attention += 10;

  if (factors.attention > 100.0f)
    factors.attention = 100.0f;

  if (newValue > 100.0f)
    newValue = 100.0f;

  *factor = (unsigned int)newValue;
  saveFactors(factors);
}

void APsetup()
{
  WiFi.disconnect(true, true);
  delay(500);

  WiFi.mode(WIFI_AP);
  WiFi.softAP("Raf", nullptr, 1, 0);
  WiFi.setTxPower(WIFI_POWER_19_5dBm);

  Serial.println("[AP] Started");
  Serial.print("[AP] IP: ");
  Serial.println(WiFi.softAPIP());

  // ----- ROOT: яйцо или панель в зависимости от hatched -----
  server.on("/", []()
            {
              if (!petConfig.hatched)
              {
                // Яйцо и кликер
                server.send(200, "text/html; charset=UTF-8", panel.renderEgg(eggClicks));
              }
              else
              {
                // Уже вылупился — обычная панель
                server.send(200, "text/html; charset=UTF-8", panel.render());
              } });

  // ----- Клик по яйцу -----
  server.on("/eggClick", []()
            {
              eggClicks++;
              if (eggClicks > 10)
                eggClicks = 10;

              String json = "{\"clicks\":" + String(eggClicks) + "}";
              server.send(200, "application/json; charset=UTF-8", json); });

  // ----- Сохранение имени и пометка, что вылупился -----
  server.on("/saveName", []()
            {
              if (server.hasArg("name"))
              {
                String name = server.arg("name");
                name.trim();
                if (name.length() > 0 && name.length() < 16)
                {
                  name.toCharArray(petConfig.name, 16);
                  petConfig.hatched = true;

                  saveConfig(petConfig);
                  panel.setName(petConfig.name);

                  Serial.print("[EGG] Saved name: ");
                  Serial.println(name);
                }
              }
              // фронтенд потом сделает window.location.href = "/"
              server.send(200, "text/plain", "OK"); });

  // ----- API для уже вылупленного тамагочи -----
  server.on("/status", []()
            {
              String json = "{";
              json += "\"hunger\":" + String(factors.hunger) + ",";
              json += "\"petting\":" + String(factors.petting) + ",";
              json += "\"attention\":" + String(factors.attention) + ",";
              json += "\"clean\":" + String(factors.clean);
              json += "}";
              server.send(200, "application/json; charset=UTF-8", json); });

  server.on("/feed", []()
            {
              if (factors.hunger < 100)
                animation.eating();
              else
                animation.enoughEating();

              increaseFactor(&factors.hunger, 25);
              saveFactors(factors);
              server.send(200, "text/plain; charset=UTF-8", "OK"); });

  server.on("/clean", []()
            {
              if (factors.clean < 100)
                animation.showering();
              else
                animation.enoughEating();

              increaseFactor(&factors.clean, 25);
              saveFactors(factors);
              server.send(200, "text/plain; charset=UTF-8", "OK"); });

  server.begin();
  Serial.println("HTTP server started.");
}

void HandleBlinking()
{
  unsigned long now = millis();

  switch (blinkState)
  {
  case 0:

    if (now - blinkTimer >= BLINK_PERIOD)
    {
      blinkState = 1;
      blinkTimer = now;
      isBlinking = true;
    }
    break;

  case 1:

    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 2;
      blinkTimer = now;
      isBlinking = false;
    }
    break;

  case 2:

    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 3;
      blinkTimer = now;
      isBlinking = true;
    }
    break;

  case 3:
    // Вторая закрытая фаза
    if (now - blinkTimer >= BLINK_TIME)
    {
      blinkState = 4;
      blinkTimer = now;
      isBlinking = false;
    }
    break;

  case 4:

    if (now - blinkTimer >= BLINK_PERIOD)
    {
      blinkState = 1;
      blinkTimer = now;
      isBlinking = true;
    }
    break;
  }
}

bool isPressed()
{
  int touch = digitalRead(TOUCH_PIN);

  if (touch == 1)
    return true;
  else if (touch == 0)
    return false;
}

